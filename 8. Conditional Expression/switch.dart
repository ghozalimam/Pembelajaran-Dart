// Switch
void main(){
String nama = "Daus";
switch(nama){
    case "Jono":
        print("nama dia adalah Jono");
        break;
    case "Sumanto":
        print("nama dia adalah Sumanto");
        break;
    case "Asep":
        print("nama dia adalah Asep");
        break;
    default:
        print("nama dia bukan Jono, Sumanto dan Asep, Tetapi $nama");
  }
}